def encrypt(value):
    """
    Default encrypter that does nothing.

    A similar module or class can be written that implements a custom method
    of encrypting the value that's given to it

    :param value: What to encrypt
    :return: The encrypted value
    """
    return value


def decrypt(value):
    """
    Default decrypter that does nothing.

    A similar module or class can be written that implements a custom method
    of encrypting the value that's given to it

    :param value: What to decrypt
    :return: The decrypted value
    """
    return value
